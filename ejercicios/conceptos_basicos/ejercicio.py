## Ejercicio 2: Operadores Aritméticos
"""
Dadas las variables `a = 10` y `b = 3`, calcula y muestra:
- La suma
- La resta
- La multiplicación
- La división
- La división entera
- El módulo (resto)
- La potencia (a elevado a b)
"""
a = 10
b = 3
suma = a + b
resta = a - b
multiplicacion = b * a
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b



print(f"suma = {suma}")
print(f"resta = {resta}")
print(f"multiplicacion = {multiplicacion}")
print(f"division = {division}")
print(f"division_entera = {division_entera}")
